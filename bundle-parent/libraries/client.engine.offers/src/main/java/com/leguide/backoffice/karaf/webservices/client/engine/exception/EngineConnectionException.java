package com.leguide.backoffice.karaf.webservices.client.engine.exception;


public class EngineConnectionException extends EngineException {

	private static final long serialVersionUID = -5654621906994289565L;

	public EngineConnectionException() {
		super();
	}

	public EngineConnectionException(String message, Throwable cause) {
		super(message, cause);
	}

	public EngineConnectionException(String message) {
		super(message);
	}

	public EngineConnectionException(Throwable cause) {
		super(cause);
	}

}
