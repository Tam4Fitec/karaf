package com.leguide.backoffice.karaf.webservices.client.engine.exception;

public class EngineIOException extends EngineException {

	private static final long serialVersionUID = 8845728699395007283L;

	public EngineIOException() {
		super();
	}

	public EngineIOException(String message, Throwable cause) {
		super(message, cause);
	}

	public EngineIOException(String message) {
		super(message);
	}

	public EngineIOException(Throwable cause) {
		super(cause);
	}

}
